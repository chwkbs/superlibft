#include "libft.h"

int		ft_atoi(const char *str)
{
	int r;
	int sign;

	r = 0;
	sign = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-' || *str == '+')
	{
		sign = (*str == '-') ? (-1) : (1);
		str++;
	}
	while (*str && (*str >= '0' && *str <= '9'))
	{
		r = r * 10 + *str - 48;
		str++;
	}
	return (sign * r);
}
